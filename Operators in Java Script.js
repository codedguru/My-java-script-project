/*Arithmetic operators in js.
+Addition 
-Subtraction
*multiplication
/Division
*/

/*Comparison operators in js.
&& To check if two statements are true.
|| To check if one out of the result is true.
! To check if the result is not true.
*/

//+Addition 
console.log(2+3+4+5+6+7+8)

//Subtraction
console.log(7-5)

//multiplication
console.log(2*3*4*5*6*7*8)

//Division
console.log(50/5)

//To check if two statements are true.
console.log(2>5 && 2<10)

//To check if one out of the result is true
console.log(2<5 || 2>5 )

//To check if the result is not true
console.log(!2 > 5)

//To check if two numbers are equal 
console.log(10 == 10)
