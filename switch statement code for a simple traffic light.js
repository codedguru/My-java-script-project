//using the switch statement
const light = 'green'
switch (light){
    case'green':
    console.log('Drive');
    break;
    case'Orange':
        console.log('Ready to go');
        break;
    case 'Red':
        console.log('Stop');
        break;
    default:
        console.log('the sign neither green, orange or red');
        break;
}